#if LW_CFG_VMM_EN > 0

    FILE_BEGIN()
    
    EXPORT_LABEL(armMmuV7SelectTtb0)
    
;/*********************************************************************************************************
;  This API Configures the TTB control register to use only TTB0
;*********************************************************************************************************/

FUNC_DEF(armMmuV7SelectTtb0)
    MOV     R0, #0
    MCR     p15, 0, R0, c2, c0, 2
    BX      LR
    FUNC_END()
    
    FILE_END()

