#ifndef __MOSIPC_H
#define __MOSIPC_H

/*********************************************************************************************************
  主操作系统初始化结构 (每个系统中只能存在一个主操作系统)
*********************************************************************************************************/
typedef struct {
    addr_t      MHP_ulSmBase;                                           /*  共享内存基地址              */
    size_t      MHP_stSize;                                             /*  共享内存大小                */
    FUNCPTR     MHP_pfuncNotify;                                        /*  通知操作系统事件            */
} LW_MOSIPC_HOSTOS_PARAM;

/*********************************************************************************************************
  从操作系统初始化结构 (每个系统中可以存在多个从操作系统)
*********************************************************************************************************/
typedef struct {
    addr_t      MHP_ulSmBase;                                           /*  共享内存基地址              */
    size_t      MHP_stSize;                                             /*  共享内存大小                */
    FUNCPTR     MHP_pfuncNotify;                                        /*  通知操作系统事件            */
} LW_MOSIPC_GUESTOS_PARAM;

#endif                                                                  /*  __MOSIPC_H                  */
